    private void jXDatePicker1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jXDatePicker1ActionPerformed
        jXDatePicker1.setEditable(false);
        Date selectedDate = new Date(jXDatePicker1.getDate().getTime()); 
        boolean flag = false;
        for (Iterator<Artist> it = csController.getAvailableMainArtistForDate(selectedDate).iterator(); it.hasNext();) {
            Artist s = it.next();
            for (Map.Entry<String, ArrayList<java.util.Date>> entry : HandsInTheAir.getXML().getOccupied().entrySet()) {
                String key = entry.getKey();
                ArrayList<java.util.Date> value = entry.getValue();
                if (key.equals(s.getArtistAlphaCode()) && value.contains(selectedDate)) {
                    flag = true;
                }
            }
            if (flag)
                continue;
            artistComboBox.addItem(s.getStageName());
            artistIDOf.put(s.getStageName(), s.getArtistAlphaCode());
        }
        for (String s : csController.getAvailablePlacesForDate(selectedDate)) {
            placeComboBox.addItem(s);
        }
        placeComboBox.addItem("---------------------");
        for (String s : csController.getPlacesExcluding(selectedDate, csController.getAvailablePlacesForDate(selectedDate))) {
            placeComboBox.addItem(s);
        }
    }//GEN-LAST:event_jXDatePicker1ActionPerformed

