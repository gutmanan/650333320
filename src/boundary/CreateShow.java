    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String artist = artistIDOf.get(String.valueOf(artistComboBox.getSelectedItem())); 
        String agent = WindowManager.getTmpAgent().getId();
        Timestamp showDate = new Timestamp(jXDatePicker1.getDate().getTime());
        String start = startTextField.getText();
        String place = String.valueOf(placeComboBox.getSelectedItem());
        String ticketPrice = ticketTextField.getText();
        String minAge = minageTextField.getText();
        int showID = csController.newShow(artist, agent, showDate, start, Integer.valueOf(place), ticketPrice, minAge);
        int counter = 0;
        int anotherCounter = 0;
        for (int i = 0; i < jTable3.getRowCount(); i++) {
            if ((boolean)jTable3.getValueAt(i, 1)) {
                counter++;
                if (csController.newArtistInvitations(showID, artistIDOf.get((String)jTable3.getValueAt(i, 0)))) {
                    anotherCounter++;
                }
            }
        }
        if (showID > -1 && counter == anotherCounter) {
            JOptionPane.showMessageDialog(null,
                "Show was created successfully!",
                "Setup complete",
                JOptionPane.INFORMATION_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

