    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jXDatePicker1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Please choose a date first!");
            return;
        }
        String artist = artistIDOf.get(String.valueOf(artistComboBox.getSelectedItem())); 
        String start = startTextField.getText();
        String place = String.valueOf(placeComboBox.getSelectedItem());
        String ticketPrice = ticketTextField.getText();
        String minAge = minageTextField.getText();
        if (artist == null || start.isEmpty() || place.isEmpty() || ticketPrice.isEmpty() || minAge.isEmpty()) {
           JOptionPane.showMessageDialog(null, "Please fill all the fields first!");
           return;
        }
        jPanel1.setVisible(true);
        jPanel1.setBounds(25, 305, 650, 260);
        Date selectedDate = new Date(jXDatePicker1.getDate().getDay(), jXDatePicker1.getDate().getMonth(), jXDatePicker1.getDate().getYear());
        DefaultTableModel model = new DefaultTableModel(); 
        jTable3.setModel(model);
        model.addColumn("Name"); 
        model.addColumn("Invite");
        TableColumn tc = jTable3.getColumnModel().getColumn(1);
        tc.setCellEditor(jTable3.getDefaultEditor(Boolean.class));
        tc.setCellRenderer(jTable3.getDefaultRenderer(Boolean.class));
        Artist tmpArtist = null;
        for (String a : csController.getAvailableAdditionalArtists(selectedDate, artistIDOf.get(String.valueOf(artistComboBox.getSelectedItem())))) {
            tmpArtist = csController.getArtistDetails(a);
            if (tmpArtist != null)
                model.addRow(new Object[]{tmpArtist.getStageName(), false});
        }
        jTable3.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (jTable3.getSelectedRow() > -1) {
                    jLabel20.setText("Agent:");
                    jLabel21.setText("Email:");
                    jLabel19.setText("Facebook:");
                    jLabel16.setText("Biography:");
                    Artist tmp = csController.getArtistDetails(artistIDOf.get(String.valueOf(jTable3.getValueAt(jTable3.getSelectedRow(),0))));
                    if (tmp != null) {
                        jLabel20.setText(jLabel20.getText()+" "+tmp.getAgent().getId());
                        jLabel21.setText(jLabel21.getText()+" "+tmp.getEmail());
                        jLabel19.setText(jLabel19.getText()+" "+tmp.getFacebook());
                        jLabel16.setText(jLabel16.getText()+" "+tmp.getBiography());
                    }
                }                
            }
        });
    }//GEN-LAST:event_jButton2ActionPerformed

